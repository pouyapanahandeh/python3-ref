# while loop 
i = 0
counter = int(input("Enter the Destination number"))
while i <= counter:
	# i += 1
	print(i)
	i += 1
print("Done")

# make pattern by while loop

j = 0
star_counter = int(input("enter the end of the pattern: "))
while j <= star_counter:
	# here we can multiply string with number, to repeat them
	print('*' * j)
	j += 1
print("Done")

