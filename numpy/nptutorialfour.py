import numpy as np

# add 2 to all the elements of the array
a = np.array([1,2,3,4,5])
print(a + 2)

# -2
print(a - 2)

# multiply by 2
print(a * 2)

# divide by 2
print(a / 2)

# addition of two arrays
b = np.array([1,2,3,4,5])
print(a + b)

# power: all element of the array to power of two
print(a ** 2)

# sin, cos and etc.
print(np.sin(a))
print(np.cos(a))
print(np.tan(a))