# for loop in python

fruits = ["apple","melon","mandarin"]
for x in fruits:
	print(x)
	
# break in for loop, wont print the element in  condition block

animal = ["horse","monkey","cat"]

for z in animal:
	if z == "monkey":
		break
	print(z)
	
# range in for loop start from 0 till the number you gave in the range 
	
for x in range(6):
	print(x)
	
for y in range(2,8):
	print(y)
	
for k in range(0, 20, 5):
	print(k)
	